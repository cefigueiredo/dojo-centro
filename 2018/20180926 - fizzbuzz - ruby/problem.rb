def fizzbuzz(number)
  if (number == 3 || number == 6 || number == 9)
    return 'fizz'

  elsif number == 2
    return 2

  elsif number == 4
    return  4

  elsif (number  == 5 || number == 10)
    return 'buzz'

  elsif number  == 7
    return 7

  elsif number  == 8
    return 8
  end 

  return 1
end